    private void botaoSalvarNovoAtendenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarNovoAtendenteActionPerformed
        if (campoIdAtendente.getText().matches("") || campoNomeAtendente.getText().matches("")  
                || campoUsuario.getText().matches("") || campoCadastroSenha.getText().matches("")
                || campoConfirmacaoSenha.getText().matches("")) {
            JOptionPane.showMessageDialog(rootPane, "√â necess√°rio preencher todos os campos!", 
                    "Pequi Cr√©dito", JOptionPane.WARNING_MESSAGE);
        } else {
        LoginController cadastro = new LoginController();
        cadastro.cadastroUsuario(this);
        }
    }//GEN-LAST:event_botaoSalvarNovoAtendenteActionPerformed

